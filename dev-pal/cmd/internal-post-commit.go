func trackTechDebt() (userMsg string, aiMsg string) {
	// 1. Get diff for the last commit
	gitCmd := exec.Command("git", "diff", "HEAD~1", "HEAD")
	diffOutput, err := gitCmd.Output()
	if err != nil {
		return // Silently fail if diff fails
	}

	// 2. Find new tech debt comments
	scanner := bufio.NewScanner(strings.NewReader(string(diffOutput)))
	var techDebtLines []string
	re := regexp.MustCompile(`^\+(.*)(TODO|FIXME|HACK):`)
	for scanner.Scan() {
		line := scanner.Text()
		if re.MatchString(line) {
			techDebtLines = append(techDebtLines, line[1:]) // Store without leading '+'
		}
	}

	// 3. If found, update TECH_DEBT.md
	if len(techDebtLines) > 0 {
		techDebtFile := "TECH_DEBT.md"
		commitHash, _ := exec.Command("git", "rev-parse", "--short", "HEAD").Output()

		// Create file if it doesn't exist
		if _, err := os.Stat(techDebtFile); os.IsNotExist(err) {
			header := "# Tech Debt Log\n\nThis file is automatically generated by dev-pal.\n\n"
			os.WriteFile(techDebtFile, []byte(header), 0644)
		}

		// Append the new entry
		f, err := os.OpenFile(techDebtFile, os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0644)
		if err != nil {
			return // Silently fail
		}
		defer f.Close()

		logDate := time.Now().Format(time.RFC1123)
		entry := fmt.Sprintf("## Found in commit `%s` on %s\n\n```\n%s\n```\n\n",
			strings.TrimSpace(string(commitHash)), logDate, strings.Join(techDebtLines, "\n"))
		
		if _, err := f.WriteString(entry); err != nil {
			return // Silently fail
		}

		// Stage the file
		exec.Command("git", "add", techDebtFile).Run()

		userMsg = fmt.Sprintf("üìù I found new tech debt and updated `%s`. It's been staged for your next commit!", techDebtFile)
		aiMsg = fmt.Sprintf("P.S. I logged some new tech debt items for you in `%s` so they don't get lost! Keep up the great work.", techDebtFile)
		return userMsg, aiMsg
	}

	return "", ""
}

